#!/usr/bin/env node
/**
 * Adds compileBindingsJS, compileBindingEntryJS, and evalBindingsJsonJS to verdict.mjs
 * for notebook per-cell eval (each nullary binding compiled as its own entry).
 * Also injects lib/verdict/Actor.verdict and lib/verdict/IDE.verdict into compileBindings.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const srcPath = path.join(root, "public/lib/verdict.mjs");
const actorPath = path.join(root, "lib/verdict/Actor.verdict");
const idePath = path.join(root, "lib/verdict/IDE.verdict");
const cellBusPath = path.join(root, "lib/verdict/CellBus.verdict");
const displayPath = path.join(root, "lib/verdict/Display.verdict");
const outPath = path.join(root, "public/lib/verdict-notebook.mjs");

let code = fs.readFileSync(srcPath, "utf8");

const patchStart = code.indexOf("\nvar compileBindingsJS = function");
let exportStart = code.lastIndexOf("\nexport {");
if (patchStart >= 0 && exportStart > patchStart) {
  code = code.slice(0, patchStart) + code.slice(exportStart);
  exportStart = code.lastIndexOf("\nexport {");
}

const actorSource = fs.readFileSync(actorPath, "utf8").trim();
const ideSource = fs.readFileSync(idePath, "utf8").trim();
const cellBusSource = fs.readFileSync(cellBusPath, "utf8").trim();
const displaySource = fs.readFileSync(displayPath, "utf8").trim();
const actorLibraryLiteral = JSON.stringify("\n" + actorSource + "\n");
const ideLibraryLiteral = JSON.stringify("\n" + ideSource + "\n");
const cellBusLibraryLiteral = JSON.stringify("\n" + cellBusSource + "\n");
const displayLibraryLiteral = JSON.stringify("\n" + displaySource + "\n");

const libraryLinkHelpers = [
  "var usesActorLibrary = function(src) {",
  "  return /\\b(spawnCounter|counterAdd|counterGet|spawnRegistry|registryPut|registryGetPid)\\b/.test(src);",
  "};",
  "var usesIdeLibrary = function(src) {",
  "  return /\\b(ensureGlobal|bootGlobal|registerWorker|ask\\b|idePut|ideGet)\\b/.test(src);",
  "};",
  "var usesCellBusLibrary = function(src) {",
  "  return /\\b(busPost|busRead|busQueue|busPending|busTakeFirst)\\b/.test(src);",
  "};",
  "var usesDisplayLibrary = function(src) {",
  "  return /\\b(dText|dTrace|dLine|dArea|dStep|dMarkers|dChart|dChartY|dChartY2|dStack|dRow|dCol|dTable)\\b/.test(src);",
  "};",
  "var parseLibraryModule = function(source) {",
  "  var v = parseVerdict(source);",
  "  if (v instanceof Right) { return v.value0; }",
  '  throw new Error("internal error: library failed to parse");',
  "};",
  "var addLibraryDecls = function(used, source, types, decls) {",
  "  if (!used) { return; }",
  "  var m = parseLibraryModule(source);",
  "  var ts = moduleTypes(m); var ds = moduleDecls(m);",
  "  for (var i = 0; i < ts.length; i++) { types.push(ts[i]); }",
  "  for (var j = 0; j < ds.length; j++) { decls.push(ds[j]); }",
  "};",
  "// Merge every used library's decls INTO the user module, then link once",
  "// against the full prelude. Reachability is then computed from the user's own",
  "// code, so prelude functions the user calls (e.g. strConcat) survive even when",
  "// a linked library does not reference them. Chaining per-library links would",
  "// instead prune the prelude down to each library's needs.",
  "var linkBindingsModule = function(src, userMod, preludeMod) {",
  "  var types = []; var decls = [];",
  "  addLibraryDecls(usesActorLibrary(src), actorLibrarySource, types, decls);",
  "  addLibraryDecls(usesIdeLibrary(src), ideLibrarySource, types, decls);",
  "  addLibraryDecls(usesCellBusLibrary(src), cellBusLibrarySource, types, decls);",
  "  addLibraryDecls(usesDisplayLibrary(src), displayLibrarySource, types, decls);",
  "  var mergedTypes = moduleTypes(userMod).concat(types);",
  "  var mergedDecls = moduleDecls(userMod).concat(decls);",
  "  var mergedUser = new Module(moduleName(userMod), mergedTypes, mergedDecls);",
  "  return linkAll(mergedUser)(preludeMod);",
  "};",
  // Render the compiler's internal type format as readable Verdict surface
  // syntax for hover / autocomplete: {[(Tuple "k" T)]} -> { k : T } and ? -> Json.
  "var splitTopLevelCommas = function(s) {",
  "  var parts = []; var depth = 0; var cur = '';",
  "  for (var i = 0; i < s.length; i++) {",
  "    var c = s[i];",
  "    if (c === '(' || c === '{' || c === '[') depth++;",
  "    else if (c === ')' || c === '}' || c === ']') depth--;",
  "    if (c === ',' && depth === 0) { parts.push(cur); cur = ''; } else cur += c;",
  "  }",
  "  if (cur.length) parts.push(cur);",
  "  return parts;",
  "};",
  "var prettyRecordInner = function(inner) {",
  "  var parts = splitTopLevelCommas(inner);",
  "  var out = [];",
  "  for (var i = 0; i < parts.length; i++) {",
  "    var p = parts[i].trim();",
  "    var m = p.match(/^\\(Tuple\\s+\"([^\"]*)\"\\s+([\\s\\S]*)\\)$/);",
  "    if (m) out.push(m[1] + ' : ' + m[2].trim()); else out.push(p);",
  "  }",
  "  return '{ ' + out.join(', ') + ' }';",
  "};",
  "var prettifyTypeSig = function(sig) {",
  "  if (typeof sig !== 'string') return sig;",
  "  var s = sig.replace(/\\?/g, 'Json');",
  "  var guard = 0;",
  "  while (s.indexOf('{[') >= 0 && guard < 60) {",
  "    guard++;",
  "    s = s.replace(/\\{\\[((?:(?!\\{\\[|\\]\\})[\\s\\S])*)\\]\\}/, function(_m, inner) { return prettyRecordInner(inner); });",
  "  }",
  "  return s;",
  "};",
  "",
].join("\n");

code = code.replace(/\nvar actorLibrarySource = "[^"]*";?\n/g, "\n");
code = code.replace(/\nvar ideLibrarySource = "[^"]*";?\n/g, "\n");
code = code.replace(/\nvar usesActorLibrary = function[\s\S]*?\n\};\nvar linkBindingsModule[\s\S]*?\n\};\n/g, "\n");
code = code.replace(
  /var preludeSource = '/,
  `var actorLibrarySource = ${actorLibraryLiteral};\nvar ideLibrarySource = ${ideLibraryLiteral};\nvar cellBusLibrarySource = ${cellBusLibraryLiteral};\nvar displayLibrarySource = ${displayLibraryLiteral};\n${libraryLinkHelpers}var preludeSource = '`,
);

code = code.replace(
  /var compileBindings = function\(src\) \{[\s\S]*?\n\};\nvar evalBindingsJS/,
  [
    "var compileBindings = function(src) {",
      "  var v = parseVerdict(src);",
      "  if (v instanceof Left) {",
      '    return new Left("Parse error: " + show17(v.value0));',
      "  }",
      "  ;",
      "  if (v instanceof Right) {",
      "    var v1 = parseVerdict(preludeSource);",
      "    if (v1 instanceof Left) {",
      '      return new Left("internal error: prelude failed to parse");',
      "    }",
      "    ;",
      "    if (v1 instanceof Right) {",
      "      var mod5;",
      "      try {",
      "        mod5 = linkBindingsModule(src, v.value0, v1.value0);",
      "      } catch (err) {",
      "        return new Left(String(err.message || err));",
      "      }",
      "      var v2 = checkModule(mod5);",
      "      if (v2 instanceof Left) {",
      '        return new Left("Type error: " + showTypeError(v2.value0));',
      "      }",
      "      ;",
      "      if (v2 instanceof Right) {",
      "        var lowered = lowerModule(monomorphize(mod5));",
      "        var emitFuncs = map211(function($108) {",
      "          return toEmitFunc(optimize($108));",
      "        })(lowered.funcs);",
      "        return new Right(assemble(emitFuncs)(lowered.entry));",
      "      }",
      "      ;",
      '      throw new Error("Failed pattern match at Verdict.Compiler (line 106, column 10 - line 113, column 53): " + [v2.constructor.name]);',
      "    }",
      "    ;",
      '    throw new Error("Failed pattern match at Verdict.Compiler (line 102, column 20 - line 113, column 53): " + [v1.constructor.name]);',
      "  }",
      "  ;",
      '  throw new Error("Failed pattern match at Verdict.Compiler (line 100, column 23 - line 113, column 53): " + [v.constructor.name]);',
      "};",
      "var evalBindingsJS",
    ].join("\n"),
);

code = code.replace(
  /var diagnosticsJS = function\(src\) \{[\s\S]*?\n\};\nvar compileProject/,
  [
    "var diagnosticsJS = function(src) {",
    "  var parseDiag = function(perr) {",
    "    var v3 = parseErrorPosition(perr);",
    "    return {",
    "      line: v3.line,",
    "      column: v3.column,",
    "      message: parseErrorMessage(perr),",
    '      severity: "error"',
    "    };",
    "  };",
    "  var v = parseVerdict(src);",
    "  if (v instanceof Left) {",
    "    return [parseDiag(v.value0)];",
    "  }",
    "  ;",
    "  if (v instanceof Right) {",
    "    var v1 = parseVerdict(preludeSource);",
    "    if (v1 instanceof Left) {",
    "      return [];",
    "    }",
    "    ;",
    "    if (v1 instanceof Right) {",
    "      var mod5;",
    "      try {",
    "        mod5 = linkBindingsModule(src, v.value0, v1.value0);",
    "      } catch (err) {",
    "        return [];",
    "      }",
    "      var v2 = checkModule(mod5);",
    "      if (v2 instanceof Left) {",
    "        var l = locate(v2.value0);",
    "        return [{",
    "          line: l.line,",
    "          column: l.column,",
    "          message: l.message,",
    '          severity: "error"',
    "        }];",
    "      }",
    "      ;",
    "      if (v2 instanceof Right) {",
    "        return [];",
    "      }",
    "      ;",
    '      throw new Error("Failed pattern match at Verdict.Compiler (line 198, column 25 - line 202, column 20): " + [v2.constructor.name]);',
    "    }",
    "    ;",
    '    throw new Error("Failed pattern match at Verdict.Compiler (line 196, column 20 - line 202, column 20): " + [v1.constructor.name]);',
    "  }",
    "  ;",
    '  throw new Error("Failed pattern match at Verdict.Compiler (line 194, column 21 - line 202, column 20): " + [v.constructor.name]);',
    "};",
    "var compileProject",
  ].join("\n"),
);

code = code.replace(
  "  return append9(sigsOf(src))(sigsOf(preludeSource));\n};",
  [
    "  var sigs = append9(sigsOf(src))(sigsOf(preludeSource));",
    "  if (usesActorLibrary(src)) {",
    "    sigs = append9(sigs)(sigsOf(actorLibrarySource));",
    "  }",
    "  if (usesIdeLibrary(src)) {",
    "    sigs = append9(sigs)(sigsOf(ideLibrarySource));",
    "  }",
    "  if (usesCellBusLibrary(src)) {",
    "    sigs = append9(sigs)(sigsOf(cellBusLibrarySource));",
    "  }",
    "  if (usesDisplayLibrary(src)) {",
    "    sigs = append9(sigs)(sigsOf(displayLibrarySource));",
    "  }",
    "  return sigs.map(function(s) { return { name: s.name, signature: prettifyTypeSig(s.signature) }; });",
    "};",
  ].join("\n"),
);

exportStart = code.lastIndexOf("\nexport {");

const insert = [
  "",
  "var nullaryUserBindingNames = function(src) {",
  "  var v = parseVerdict(src);",
  "  if (v instanceof Left) {",
  "    return [];",
  "  }",
  "  return mapMaybe(function(v2) {",
  "    var $93 = $$null(v2.params);",
  "    if ($93) {",
  "      return new Just(v2.name);",
  "    }",
  "    return Nothing.value;",
  "  })(moduleDecls(v.value0));",
  "};",
  "var reorderBindingFirstInSource = function(src, entryName) {",
  "  var lines = src.split(/\\n/);",
  "  var header = [];",
  "  var i = 0;",
  "  while (i < lines.length) {",
  "    var t = lines[i].trim();",
  "    if (t.startsWith('module ') || t === '') {",
  "      header.push(lines[i]);",
  "      i += 1;",
  "      continue;",
  "    }",
  "    break;",
  "  }",
  "  var body = lines.slice(i).join('\\n');",
  "  var blocks = body.split(/\\n\\s*\\n/).map(function(b) { return b.trim(); }).filter(Boolean);",
  "  var bindingIdent = function(line) {",
  "    var t = line.trim();",
  "    if (!t || t.startsWith('--')) return '';",
  "    var m = t.match(/^([A-Za-z_][A-Za-z0-9_']*)(?:\\s+[:(]|\\s+=|$)/);",
  "    return m ? m[1] : '';",
  "  };",
  "  var blockName = function(block) {",
  "    var rows = block.split('\\n');",
  "    for (var r = 0; r < rows.length; r += 1) {",
  "      var n = bindingIdent(rows[r]);",
  "      if (n) return n;",
  "    }",
  "    return '';",
  "  };",
  "  var targetIdx = -1;",
  "  for (var b = 0; b < blocks.length; b += 1) {",
  "    if (blockName(blocks[b]) === entryName) {",
  "      targetIdx = b;",
  "      break;",
  "    }",
  "  }",
  "  if (targetIdx < 0) return src;",
  "  var target = blocks[targetIdx];",
  "  // The underlying compiler always picks a binding named `main` as the program",
  "  // entrypoint when one exists, otherwise the first declaration. So merely",
  "  // reordering the target to the top is not enough when the program also",
  "  // defines `main` (e.g. notebook cell 1). Drop the `main` block when the",
  "  // requested entry is something else so the reordered head wins and the",
  "  // target binding becomes the entrypoint (its deps stay reachable; main's",
  "  // own subtree is tree-shaken away).",
  "  var rest = blocks.filter(function(blk, idx) {",
  "    if (idx === targetIdx) return false;",
  "    if (entryName !== 'main' && blockName(blk) === 'main') return false;",
  "    return true;",
  "  });",
  "  var headerText = header.join('\\n').replace(",
  "    /module\\s+([A-Za-z][A-Za-z0-9_]*)\\s+exposing\\s*\\([^)]*\\)/,",
  "    function(_m, modName) {",
  "      return 'module ' + modName + ' exposing (..)';",
  "    }",
  "  );",
  "  return [headerText.trim(), target.trim()].concat(rest.map(function(b) { return b.trim(); })).filter(Boolean).join('\\n\\n');",
  "};",
  "var compileBindingEntry = function(src, entryName) {",
  "  var reordered = reorderBindingFirstInSource(src, entryName);",
  "  return compileBindings(reordered);",
  "};",
  "var compileBindingEntryJS = function(src, entryName) {",
  "  var v = compileBindingEntry(src, entryName);",
  "  if (v instanceof Right) {",
  "    return {",
  "      ok: true,",
  "      output: stringifyWithIndent(2)(encodeProgramVM(v.value0)),",
  '      error: ""',
  "    };",
  "  }",
  "  if (v instanceof Left) {",
  '    return { ok: false, output: "", error: v.value0 };',
  "  }",
  '  throw new Error("compileBindingEntryJS: unexpected result");',
  "};",
  "var compileBindingsJS = function(src) {",
  "  var names = nullaryUserBindingNames(src);",
  "  if (names.length === 0) {",
  "    var v0 = compileBindings(src);",
  "    if (v0 instanceof Right) {",
  "      return {",
  "        ok: true,",
  "        output: stringifyWithIndent(2)(encodeProgramVM(v0.value0)),",
  '        error: ""',
  "      };",
  "    }",
  "    if (v0 instanceof Left) {",
  '      return { ok: false, output: "", error: v0.value0 };',
  "    }",
  '    throw new Error("compileBindingsJS: unexpected result");',
  "  }",
  "  return compileBindingEntryJS(src, names[0]);",
  "};",
  "var nullaryBindingsJS = function(src) {",
  "  return nullaryUserBindingNames(src);",
  "};",
  "var evalBindingsJsonJS = function(src, names) {",
  "  var userNames = nullaryUserBindingNames(src);",
  "  var userNameSet = {};",
  "  for (var u = 0; u < userNames.length; u += 1) userNameSet[userNames[u]] = true;",
  "  var sigMap = {};",
  "  signaturesJS(src).forEach(function(s) {",
  "    if (userNameSet[s.name]) sigMap[s.name] = s.signature;",
  "  });",
  "  var filterNames = names && names.length ? names : null;",
  "  var targetNames = filterNames || nullaryUserBindingNames(src);",
  "  var results = [];",
  "  for (var i = 0; i < targetNames.length; i += 1) {",
  "    var name2 = targetNames[i];",
  "    if (filterNames && filterNames.indexOf(name2) < 0) {",
  "      continue;",
  "    }",
  "    var c = compileBindingEntry(src, name2);",
  "    if (c instanceof Left) {",
  "      results.push({",
  "        name: name2,",
  "        ok: false,",
  "        json: null,",
  '        typeSig: sigMap[name2] || "",',
  "        error: c.value0",
  "      });",
  "      continue;",
  "    }",
  "    var v2 = runProgram(c.value0);",
  "    if (v2 instanceof Right) {",
  "      results.push({",
  "        name: name2,",
  "        ok: true,",
  "        json: encodeValueJson(v2.value0),",
  '        typeSig: sigMap[name2] || "",',
  '        error: ""',
  "      });",
  "    } else if (v2 instanceof Left) {",
  "      results.push({",
  "        name: name2,",
  "        ok: false,",
  "        json: null,",
  '        typeSig: sigMap[name2] || "",',
  "        error: v2.value0",
  "      });",
  "    } else {",
  '      throw new Error("evalBindingsJsonJS: unexpected runProgram result");',
  "    }",
  "  }",
  "  return results;",
  "};",
  "",
].join("\n");

code = code.slice(0, exportStart) + insert + code.slice(exportStart);
if (!code.includes("  compileBindingsJS,")) {
  code = code.replace(
    "  evalBindingsJS,",
    "  evalBindingsJS,\n  compileBindingsJS,\n  compileBindingEntryJS,\n  nullaryBindingsJS,\n  evalBindingsJsonJS,",
  );
}

fs.writeFileSync(outPath, code);
console.log("Wrote", outPath);
